function genericBody(row) {
  const name = trimText(row.name);
  const category1 = trimText(row.category1);
  const category2 = trimText(row.category2);
  const address = trimText(row.address);
  const base = [];

  if (category1 === 'MICE 기획 · 운영분과') {
    if (category2.includes('여행사')) return travelTemplate(row);
    return planningTemplate(row);
  }

  if (category1 === 'MICE 지원분과') {
    if (name.includes('병원')) return medicalTemplate(row);
    if (name.includes('미술관') || name.includes('재단') || name.includes('연구원') || name.includes('대학교') || name.includes('학과')) {
      return cultureTemplate(row);
    }
    if (name.includes('소주') || name.includes('카페') || name.includes('케이터링') || name.includes('유키친') || name.includes('푸드') || name.includes('오븐스토리')) {
      return foodTemplate(row);
    }
    return supportTemplate(row);
  }

  if (category1 === 'MICE 시설분과') {
    base.push(`${name}은(는) ${address || '대전·충청 지역'}을 기반으로 운영되는 MICE 관련 시설 및 서비스 회원사로, 참가자 편의와 행사 운영 안정성을 함께 고려하는 공간과 서비스를 제공합니다.`);
    base.push('시설형 회원사는 행사의 시작점과 끝점에서 모두 중요한 역할을 합니다. 숙박, 이동, 공간 활용, 편의 지원, 현장 응대와 같은 요소가 안정적으로 연결되어야 참가자가 체감하는 경험도 좋아집니다.');
    base.push(`행사 주최 측은 ${name} 같은 시설 파트너와 협업함으로써 운영의 빈틈을 줄이고, 보다 예측 가능한 일정과 품질을 만들 수 있습니다. 지역 MICE 인프라 안에서 실용성과 신뢰를 함께 보여 주는 역할이 큽니다.`);
    return base;
  }

  return cultureTemplate(row);
}

function buildContent(row) {
  if (specialBodies[row.name]) {
    return [...specialBodies[row.name].paragraphs, ...buildLinkLines(row.name)].join('\n\n');
  }

  const paragraphs = genericBody(row);
  const links = buildLinkLines(row.name);
  return [...paragraphs, ...links].join('\n\n');
}

const statements = rows.map((row) => {
  const content = buildContent(row);
  return [
    `-- ${row.name}`,
    `UPDATE alliance_members`,
    `SET content = $ALLIANCE$${content}$ALLIANCE$`,
    `WHERE id = '${row.id}';`,
  ].join('\n');
}).join('\n\n');

fs.writeFileSync(outputPath, statements + '\n', 'utf8');
console.log(`Wrote ${rows.length} updates to ${outputPath}`);
